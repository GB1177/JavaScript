//tipos primitivos

//OBS: typeof mostra o tipo da variavel no console.

// boolean

var vOuF = false;
console.log(typeof(vOuF));

// number

var numeroQualquer = 1;
console.log(typeof(numeroQualquer));

// string
var nome = "GB"
console.log(typeof(nome));

